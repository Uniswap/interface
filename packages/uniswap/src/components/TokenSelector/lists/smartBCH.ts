// Function to convert a Token (Currency) to a TokenOption
export function toTokenOption(token: Token): TokenOption {
  const logoUrl = logoUriMap[token.address as keyof typeof logoUriMap] // Get the logoUrl from the map
  return {
    currencyInfo: {
      currency: token,
      address: token.address,
      symbol: token.symbol,
      name: token.name,
      currencyId: token.chainId + '-' + token.address, // Assuming the address is accessible on the Token object
      safetyInfo: undefined, // As per instruction, set to undefined
      spamCode: undefined, // As per instruction, set to undefined
      logoUrl: logoUrl, // Use the provided logoUrl
      isSpam: undefined, // As per instruction, set to undefined
    },
    quantity: null, // No balance data, set to null
    balanceUSD: undefined, // As per instruction, set to undefined
    isUnsupported: false, // Assume supported unless specified
  }
}

